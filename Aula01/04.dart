void main() {
  double numeroDouble = 12.56;
  int numeroDoubleToInt = numeroDouble.toInt();

  int numeroInt = 5;
  double numeroIntToDouble = numeroInt.toDouble();
  
  print("Número double: $numeroDouble");
  print("Número convertido para int: $numeroDoubleToInt");

  print("\nNúmero int: $numeroInt");
  print("Número int convertido para double: $numeroIntToDouble");
}
